//(if)
let age = 20;

if (age > 18) {
    console.log("You can vote");
}

//if-else Statement
let Age = 16;

if (Age >= 18) {
    console.log("Adult");
} else {
    console.log("Minor");
}

//else-if Statement
let marks = 75;

if (marks >= 90) {
    console.log("Grade A");
} else if (marks >= 70) {
    console.log("Grade B");
} else if (marks >= 50) {
    console.log("Grade C");
} else {
    console.log("Fail");
}