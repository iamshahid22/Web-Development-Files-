let x = 10;

x += 5;  // x = x + 5 → 15
x -= 2;  // 13
x *= 2;  // 26
x /= 2;  // 13
x %= 3;  // remainder

console.log(x+=5);

//Comparison Operators
console.log(5 == "5");   // true (loose equality)
console.log(5 === "5");  // false (strict equality)

console.log(10 > 5);   // true
console.log(10 < 5);   // false
console.log(10 >= 10); // true
console.log(10 != 5);  // true